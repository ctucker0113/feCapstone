import { clientCredentials } from '../utils/client';

const endpoint = clientCredentials.databaseURL;

const getUserParties = (uid) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/Parties.json?orderBy="uid"&equalTo="${uid}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

const getSingleParty = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/authors/${firebaseKey}.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const deleteParty = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/Parties/${firebaseKey}.json`, {
    method: 'DELETE',
    headers: {
      'Content-Type': 'application/json',
    },
  }).then((response) => response.json())
    .then((data) => resolve((data)))
    .catch(reject);
});

export {
  getUserParties,
  getSingleParty,
  deleteParty,
};
